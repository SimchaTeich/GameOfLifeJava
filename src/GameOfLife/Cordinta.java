package GameOfLife;

public class Cordinta
{
    private int _x;
    private int _y;

    /**
     * C'TOR
     * */
    public Cordinta(int x, int y)
    {
        this._x = x;
        this._y = y;
    }

    // getter
    public int getX()
    {
        return this._x;
    }

    // getter
    public int getY()
    {
        return this._y;
    }
}
